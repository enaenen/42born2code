#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	len;
	size_t	i;
	char	*buf;

	if (!s || !f)
		return (NULL);
	len = ft_strlen(s);
	buf = (char *)malloc(len + 1);
	if (!buf)
		return (NULL);
	i = 0;
	while (s[i])
	{
		buf[i] = f(i, s[i]);
		i++;
	}
	buf[i] = 0;
	return (buf);
}
